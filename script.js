// Single unified logic: build UI, loader, and toggle behavior
(() => {
  const loader = document.getElementById('loader');
  const main = document.querySelector('.container');
  const extra = document.querySelector('.extra-resources');
  const footer = document.querySelector('.footer');
  const container = document.getElementById('trimesterContainer');

  const delay = (ms) => new Promise(r => setTimeout(r, ms));
  const domReady = new Promise(res => {
    if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', res, { once: true });
    else res();
  });
  const windowLoaded = new Promise(res => {
    if (document.readyState === 'complete') res();
    else window.addEventListener('load', res, { once: true });
  });

  const isHidden = (el) => getComputedStyle(el).display === 'none';
  const nextByClass = (start, className) => {
    let el = start.nextElementSibling;
    while (el) {
      if (el.classList?.contains(className)) return el;
      el = el.nextElementSibling;
    }
    return null;
  };

  // Smooth folder animations
  function expandFolder(folder) {
    // Ensure element is displayable before measuring height
    if (folder.style.display === 'none') {
      folder.style.display = 'block';
    }
    folder.classList.remove('collapsed');
    folder.classList.add('expanded');
    folder.style.height = 'auto';
    const target = folder.scrollHeight + 'px';
    folder.style.height = '0px';
    folder.getBoundingClientRect(); // reflow
    folder.style.opacity = '1';
    folder.style.height = target;
    const onEnd = (e) => {
      if (e.propertyName !== 'height') return;
      folder.style.height = 'auto';
      folder.removeEventListener('transitionend', onEnd);
    };
    folder.addEventListener('transitionend', onEnd);
  }
  function collapseFolder(folder) {
    const start = folder.scrollHeight + 'px';
    folder.style.height = start;
    folder.getBoundingClientRect(); // reflow
    folder.style.opacity = '0';
    folder.style.height = '0px';
    // Keep display:block to allow future smooth expansion; visibility handled via height/opacity
    if (folder.style.display === 'none') {
      folder.style.display = 'block';
    }
    const onEnd = (e) => {
      if (e.propertyName !== 'height') return;
      folder.classList.add('collapsed');
      folder.classList.remove('expanded');
      folder.style.height = '0px';
      folder.removeEventListener('transitionend', onEnd);
    };
    folder.addEventListener('transitionend', onEnd);
  }
  function toggleFolder(folder) {
    folder.classList.contains('expanded') ? collapseFolder(folder) : expandFolder(folder);
    // Smooth scroll newly expanded folder into view without big jumps
    if (folder.classList.contains('expanded')) {
      folder.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
    }
  }

  domReady.then(() => {
    // Build trimester -> courses -> resources
    if (container && !container.hasChildNodes()) {
      const frag = document.createDocumentFragment();
      // keep a flat index of course buttons for suggestions
      const allCourses = [];
      Object.entries(materials).forEach(([trimester, courses]) => {
  const triBtn = document.createElement('button');
  triBtn.className = 'button trimester-btn';
  triBtn.type = 'button';
  triBtn.textContent = trimester;
  triBtn.dataset.orig = trimester;
  triBtn.setAttribute('aria-expanded', 'false');

        const courseWrap = document.createElement('div');
        courseWrap.className = 'section course';
        courseWrap.style.display = 'none';

        Object.entries(courses).forEach(([courseName, items]) => {
          const cBtn = document.createElement('button');
          cBtn.className = 'button course-btn';
          cBtn.type = 'button';
          cBtn.textContent = courseName;
          cBtn.dataset.orig = courseName;
          cBtn.setAttribute('aria-expanded', 'false');
          // index for suggestions (store link to its trimester section and button)
          allCourses.push({
            name: courseName,
            btn: cBtn,
            trimesterBtn: triBtn,
            section: courseWrap
          });

          const folder = document.createElement('div');
          folder.className = 'section folder collapsed';
          folder.style.height = '0px';
          folder.style.opacity = '0';

          const grid = document.createElement('div');
          grid.className = 'button-grid';
          items.forEach(item => {
            const a = document.createElement('a');
            a.href = item.url;
            a.target = '_blank';
            a.rel = 'noopener noreferrer';
            a.textContent = item.name;
            a.dataset.orig = item.name;
            a.className = 'resource-button';
            grid.appendChild(a);
          });
          folder.appendChild(grid);
          courseWrap.appendChild(cBtn);
          courseWrap.appendChild(folder);

          // Build fast search index entry
          const resElems = Array.from(grid.children);
          const indexEntry = {
            trimesterBtn: triBtn,
            courseBtn: cBtn,
            courseSection: courseWrap,
            folder,
            courseText: courseName,
            courseLower: courseName.toLowerCase(),
            resources: resElems.map(el => ({
              el,
              text: el.dataset.orig || el.textContent,
              lower: (el.dataset.orig || el.textContent).toLowerCase()
            }))
          };
          (container._searchIndex ||= []).push(indexEntry);
        });

        frag.appendChild(triBtn);
        frag.appendChild(courseWrap);
      });
      container.appendChild(frag);
      // attach index to container for later suggestion use
      container._allCourses = allCourses;
    }
    // Hide all course sections initially
    document.querySelectorAll('.course').forEach(sec => { sec.style.display = 'none'; });

    // Collapse all folders initially
    document.querySelectorAll('.folder').forEach(f => {
      f.classList.add('collapsed');
      f.classList.remove('expanded');
      f.style.height = '0px';
      f.style.opacity = '0';
    });

    // Delegated clicks
    document.addEventListener('click', (e) => {
      // Ignore resource links
      if (e.target.closest('.resource-button, a[href]')) return;

      // Course-level buttons (inside .course): toggle the next .folder
      const courseBtn = e.target.closest('.course .button, .course button');
      if (courseBtn) {
        const folder = nextByClass(courseBtn, 'folder') || courseBtn.parentElement.querySelector('.folder');
        if (folder) {
          // Auto-collapse any other expanded folders in same course section for cleaner UX
          const siblings = courseBtn.parentElement.querySelectorAll('.folder.expanded');
          siblings.forEach(sib => { if (sib !== folder) collapseFolder(sib); });
          toggleFolder(folder);
          courseBtn.setAttribute('aria-expanded', folder.classList.contains('expanded') ? 'true' : 'false');
        }
        return;
      }

      // Trimester buttons (any .button not inside .course): toggle the next .course
      const trimesterBtn = e.target.closest('.button:not(.resource-button)');
      if (trimesterBtn && !trimesterBtn.closest('.course')) {
        // aria-controls or data-target takes priority
        const targetId = trimesterBtn.getAttribute('aria-controls') || trimesterBtn.dataset.target;
        let courseSection = targetId ? document.getElementById(targetId) : null;

        // Fallback to adjacent .course after the button
        if (!courseSection) {
          courseSection = nextByClass(trimesterBtn, 'course') ||
                          trimesterBtn.parentElement.querySelector(':scope > .course');
        }

        if (courseSection) {
          courseSection.style.display = isHidden(courseSection) ? 'block' : 'none';
          trimesterBtn.setAttribute('aria-expanded', !isHidden(courseSection) ? 'true' : 'false');
        }
      }
    });

    // --- Search functionality (debounced, highlights, keyboard support) ---
    const searchInput = document.getElementById('searchInput');
    const searchClear = document.getElementById('searchClear');
    const searchCount = document.getElementById('searchCount');

    const escapeHTML = (str) => String(str)
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;')
      .replace(/'/g, '&#39;');

    const highlight = (text, q) => {
      if (!q) return escapeHTML(text);
      try {
        const r = new RegExp('(' + q.replace(/[.*+?^${}()|[\]\\]/g, '\\$&') + ')', 'ig');
        return escapeHTML(text).replace(r, '<span class="match">$1</span>');
      } catch (e) {
        return escapeHTML(text);
      }
    };

    const debounce = (fn, ms = 200) => {
      let t;
      return (...args) => { clearTimeout(t); t = setTimeout(() => fn(...args), ms); };
    };

    const performSearch = (raw) => {
      const q = String(raw || '').trim().toLowerCase();
      const index = container._searchIndex || [];
      let totalMatches = 0;
      if (!q) {
        // Reset view fast
        index.forEach(entry => {
          entry.courseBtn.style.display = '';
          entry.courseBtn.innerHTML = escapeHTML(entry.courseText);
          collapseFolder(entry.folder);
        });
        // Hide all course sections initially again
        new Set(index.map(e => e.courseSection)).forEach(cs => cs.style.display = 'none');
        new Set(index.map(e => e.trimesterBtn)).forEach(tb => tb.style.display = '');
        if (searchCount) searchCount.textContent = '';
        return;
      }

      // Track trimester visibility
      const trimesterHasMatch = new Map();
      index.forEach(entry => {
        let matchedResources = 0;
        const courseHit = entry.courseLower.includes(q);
        // Filter resources without DOM queries
        entry.resources.forEach(r => {
          if (r.lower.includes(q) || courseHit) {
            r.el.style.display = '';
            r.el.innerHTML = highlight(r.text, q);
            if (r.lower.includes(q)) matchedResources++;
          } else {
            r.el.style.display = 'none';
            r.el.innerHTML = escapeHTML(r.text);
          }
        });

        if (courseHit || matchedResources > 0) {
          entry.courseBtn.style.display = '';
          entry.courseBtn.innerHTML = highlight(entry.courseText, q);
          // Ensure course section visible
          entry.courseSection.style.display = 'block';
          // Expand folder only if resource matched (avoid opening all on broad course match)
          if (matchedResources > 0) {
            expandFolder(entry.folder);
          } else {
            collapseFolder(entry.folder);
          }
          trimesterHasMatch.set(entry.trimesterBtn, true);
          totalMatches += (courseHit ? 1 : 0) + matchedResources;
        } else {
          entry.courseBtn.style.display = 'none';
          entry.courseBtn.innerHTML = escapeHTML(entry.courseText);
          collapseFolder(entry.folder);
          if (!trimesterHasMatch.has(entry.trimesterBtn)) {
            trimesterHasMatch.set(entry.trimesterBtn, false);
          }
        }
      });

      // Update trimester button + section visibility based on matches
      const seenTrimester = new Set();
      index.forEach(entry => {
        if (seenTrimester.has(entry.trimesterBtn)) return;
        seenTrimester.add(entry.trimesterBtn);
        const has = trimesterHasMatch.get(entry.trimesterBtn);
        if (has) {
          entry.trimesterBtn.style.display = '';
          entry.trimesterBtn.setAttribute('aria-expanded','true');
        } else {
          entry.trimesterBtn.style.display = 'none';
          entry.trimesterBtn.setAttribute('aria-expanded','false');
          entry.courseSection.style.display = 'none';
        }
      });

      if (searchCount) searchCount.textContent = `${totalMatches} result${totalMatches===1?'':'s'}`;
    };

    const debouncedSearch = debounce((e) => performSearch(e.target.value), 200);
    if (searchInput) {
      searchInput.addEventListener('input', debouncedSearch);
      searchInput.addEventListener('keydown', (e) => {
        if (e.key === 'Escape') { searchInput.value = ''; performSearch(''); searchInput.blur(); }
        if (e.key === 'Enter') {
          const first = document.querySelector('.resource-button:not([style*="display: none"])');
          if (first) first.focus();
        }
      });
    }
    if (searchClear) searchClear.addEventListener('click', () => { if (searchInput) { searchInput.value = ''; performSearch(''); searchInput.focus(); } });

    // --- Suggestions (most related courses) ---
    const suggestEl = document.getElementById('searchSuggest');
    let activeIndex = -1;

    const courseScore = (name, q) => {
      const n = name.toLowerCase();
      const i = n.indexOf(q);
      if (i === 0) return 100 - (n.length - q.length); // best: starts with
      if (i > 0) return 80 - i; // contains later
      // loose token match: give small score if all chars appear in order
      let p = 0; let hits = 0;
      for (let c of n) { if (c === q[p]) { p++; hits++; if (p === q.length) break; } }
      return hits >= Math.min(2, q.length) ? 50 + hits : -1;
    };

    const renderSuggestions = (q) => {
      if (!suggestEl) return;
      if (!q || q.length < 2) {
        suggestEl.hidden = true;
        suggestEl.innerHTML = '';
        activeIndex = -1;
        return;
      }
      const list = (container._allCourses || []).map(c => ({
        c,
        score: courseScore(c.name, q)
      })).filter(x => x.score >= 0)
        .sort((a,b) => b.score - a.score)
        .slice(0, 6);

      if (!list.length) {
        suggestEl.hidden = true;
        suggestEl.innerHTML = '';
        activeIndex = -1;
        return;
      }
      suggestEl.hidden = false;
      suggestEl.innerHTML = list.map((x, idx) => `
        <div class="suggest-item${idx===0?' active':''}" role="option" data-index="${idx}">
          <span>${x.c.name.replace(new RegExp(`(${q.replace(/[.*+?^${}()|[\\]\\]/g,'\\$&')})`,'ig'), '<span class="match">$1</span>')}</span>
          <span class="suggest-pill">Course</span>
        </div>
      `).join('');
      activeIndex = 0;
      // click handling
      suggestEl.querySelectorAll('.suggest-item').forEach((el, i) => {
        el.addEventListener('mousedown', (e) => { // use mousedown so blur doesn't hide before click
          e.preventDefault();
          pickSuggestion(list[i].c);
        });
      });
    };

    const setActive = (idx) => {
      const items = suggestEl?.querySelectorAll('.suggest-item');
      if (!items || !items.length) return;
      items.forEach(el => el.classList.remove('active'));
      activeIndex = (idx + items.length) % items.length;
      items[activeIndex].classList.add('active');
      items[activeIndex].scrollIntoView({ block: 'nearest' });
    };

    const pickSuggestion = (entry) => {
      if (!entry) return;
      // Ensure trimester's course section is visible
      if (entry.section && entry.section.style.display === 'none') {
        entry.section.style.display = 'block';
        entry.trimesterBtn?.setAttribute('aria-expanded','true');
      }
      // Focus course button
      entry.btn?.focus({ preventScroll: true });
      // Auto expand its folder for quick access
      const folder = entry.btn?.nextElementSibling?.classList.contains('folder') ? entry.btn.nextElementSibling : null;
      if (folder && !folder.classList.contains('expanded')) {
        expandFolder(folder);
        entry.btn.setAttribute('aria-expanded','true');
      }
      // Smooth scroll to the course
      entry.btn?.scrollIntoView({ behavior:'smooth', block:'center' });
      // Hide suggestions but keep search input value
      if (suggestEl) { suggestEl.hidden = true; suggestEl.innerHTML = ''; activeIndex = -1; }
    };

    // Hook suggestions to input
    if (searchInput) {
      searchInput.addEventListener('input', (e) => {
        const q = e.target.value.trim().toLowerCase();
        renderSuggestions(q);
      });
      searchInput.addEventListener('keydown', (e) => {
        const items = suggestEl?.querySelectorAll('.suggest-item') || [];
        if (!items.length) return;
        if (e.key === 'ArrowDown') { e.preventDefault(); setActive(activeIndex + 1); }
        if (e.key === 'ArrowUp')   { e.preventDefault(); setActive(activeIndex - 1); }
        if (e.key === 'Enter')     { e.preventDefault(); pickSuggestion((container._allCourses||[]).find(c => c.name === items[activeIndex]?.textContent.trim())); }
        if (e.key === 'Escape')    { suggestEl.hidden = true; suggestEl.innerHTML = ''; activeIndex = -1; }
      });
      searchInput.addEventListener('blur', () => {
        // slight delay to allow mousedown selection
        setTimeout(() => { if (suggestEl) { suggestEl.hidden = true; suggestEl.innerHTML = ''; activeIndex = -1; } }, 120);
      });
    }
  });

  // Loader + smooth reveal
  const MIN_LOADER_MS = 1400;
  Promise.all([domReady, windowLoaded, delay(MIN_LOADER_MS)]).then(() => {
    if (main) {
      main.style.display = 'block';
      requestAnimationFrame(() => {
        main.classList.add('reveal');
        extra?.classList.add('reveal');
        footer?.classList.add('reveal');
      });
    }
    if (loader) {
      const hide = () => { loader.style.display = 'none'; loader.removeEventListener('transitionend', hide); };
      loader.classList.add('fade-out');
      loader.addEventListener('transitionend', hide);
      setTimeout(hide, 1000); // fallback
    }
  });
})();
